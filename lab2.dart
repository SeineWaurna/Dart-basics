void main()
{
  int a = 2;
  int b = 3;
  
  //Adding a and b
  var c = a + b;
  print("Sum of a and b is $c");
  
  //subtracting a and b
  var d = a - b;
  print("The different a and b is $d");
  
  //Using unary minus
  var e = -d;
  print("The negation of difference between a and b is $e");
  
  //Multiplication of a and b
  var f = a * b;
  print("The product of a and b is $f");
  
  //Division of a and b
  var g = b / a;
  print("The quotient of a and b is $g");
  
  //Rememeber of a and b
  var i = b % a;
  print("The remainder of a and b is $i");
}
