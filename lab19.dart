// forEach

void main() {
  var x = [1, 2, 3, 4, 5];
  x.forEach((var num) => print(num));
  // เขียนโดยใช้ forEach จะเขียนรวบในบรรทัดเดียว โดยกำหนดสิ่งที่จะทำในภายหลัง =>
  // x เป็นตัวแปร Array (iterator)
  // x.forEach() ขอให้ไล่ดูข้อมูลในแต่ละ element ของตัวแปร x (ซึ่งเป็น Array)
  // (var num) เราต้องกำหนดตัวแปร ใช้เป็น loop สำหรับไล่ลำดับข้อมูลจากตัวแปร x
  // => เป็นเตรื่องหมายเริ่มต้นใหเทำงานที่เราจะกำหนดให้ทำ
  // print(num) ให้พิมพ์ค่าจากตัวแปร num
}
